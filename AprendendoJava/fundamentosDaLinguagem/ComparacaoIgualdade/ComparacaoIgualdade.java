public class ComparacaoIgualdade {

	
	public static void main(String[] args){
		//comparações: {> maior} {>= maior ou igual} { < menor} {<= menor ou igual}
		//igualdade: {== igual} {!= diferente}
		
		
		int a = 49;
		int b = 93;
		int c = 73;
		
		boolean maior= b > a;
		boolean maiorOuIgual = c >= a;
		boolean menor = c < a;
		boolean menorOuIgual = b <= c;
		boolean igual = a == b;
		boolean diferente = b != c;
		
		System.out.println (maior);
		System.out.println (maiorOuIgual);
		System.out.println (menor);
		System.out.println (menorOuIgual);
		System.out.println (igual);
		System.out.println (diferente);
		
		System.out.println ("===================!====================");
		
		
		
		
	
	
	
	}


}
